import math

PI = 3.14
x = 1
y = 2
z = 3

print(round(PI)) # rounding to the nearest value
print(math.ceil(PI)) # rounding up 1 value
print(math.floor(PI)) # rounding down 1 value
print(abs(PI)) # absolute value
print(pow(PI,2)) # exponential
print(math.sqrt(16)) # square root
print(max(x,y,z)) # writes the highest value
print(min(x,y,z)) # writes the smallest value